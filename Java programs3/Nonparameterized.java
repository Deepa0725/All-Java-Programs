public class Nonparameterized 
{
    //Non-Parameterized method
    public void dispalyaMessage()
    {
        System.out.println("Hello,this is a Non-Parameterized method");
    }
    public static void main(String args[])
    {
        Nonparameterized example=new Nonparameterized();
        example.dispalyaMessage(); //Calling the Nonparameterized method
    }
}
